class Mom:
    def __init__(self, cook, nurture, plan):
        self.cook = cook
        self.nurture = nurture
        self.plan = plan
    def setCook(self, cook):
        self.cook = cook
    def getCook(self):
        return self.cook
    def setNurture(self, nurture):
        self.nurture = nurture 
    def getNurture(self):
        return self.nurture 
    def setPlan(self, plan):
        self.plan = plan
    def getPlan(self):
        return self.plan

