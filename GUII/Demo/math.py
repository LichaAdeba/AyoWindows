def dividepi(x,y):
    return x/y

def divide(x,p):
    return x/p

def multiply(x,y):
    return x * y
def calculatesquare(x):
    return x * x

def calculatetriangle(x):
    return  0.5 * x * x

def calculateCircle(f , e, x):
    return (f/e) * (x*x)

def calculateCube(x):
    return 6*(x * x)